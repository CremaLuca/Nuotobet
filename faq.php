    <h1 class="h2 text-center">FAQ (Domande frequenti)</h1>
    <h3 class="h3">1. Cosa è NuotoBet?</h3>
    <p>Nuotobet è un sito di scommesse con crediti virtuali sulle gare regionali di nuoto della regione Veneto.</p>
    <h3 class="h3">2. Come funziona?</h3>
    <p>Partecipare a NuotoBet è semplice, basta <a href='/landing.php'>registrarsi</a>, scegliere una manifestazione non ancora
    svolta, selezionare una delle gare di quella manifestazione e scegliere un atleta che partecipa a quella gara. Una volta
    trovato basta cliccare su "Visualizza" per visualizzare la pagina della partecipazione dell'atleta e scegliere che tipo di
    scommessa assegnare a quella gara: esistono 6 tipi</p>
    <ul>
        <li>"<<" - Molto meno</li>
        <li>"<" - Meno</li>
        <li>"≃" - Uguale</li>
        <li>">" - Di più</li>
        <li>">>" - Molto di più</li>
        <li>"DSQ" - Squalifica</li>
    </ul>
    <p>Il tempo di riferimento è il tempo di iscrizione.</p>
    <p>Quando si ha scelto che tipo di scomessa si vuole piazzare basta cliccare sul pulsante corrispondente per aggiungerla
    ad una nuova schedina oppure a quella in corso di completamento. Quando si ha accumulato qualche scommessa su una
    schedina aperta, bisogna chiuderla prima che la manifestazione si svolga perchè le sue scommesse vengano salvate e prese
    in considerazione dal sistema.</p>
    <h3 class="h3">3. Non riesco a scommettere, non si possono premere i pulsanti</h3>
    <p>Se i pulsanti non sono cliccabili è molto probabile che non si abbia fatto l'accesso, per rimediare basta andare
    alla <a href="/landing.php">pagina di accesso</a> ed effettuare l'accesso con un account esistente oppure registrarne uno nuovo.</p>
